                      <p>There is no one agency that can be blamed for this situation. Simple truth is that a small and marginal farmers can't conduct farming by himself. We need to institutions that will aggregate poor farmers agricultural produce in an organised and planned manner and provide them good return for it so they can support their family.  </p>
